let nome = ' Lucas Henrique '

//propriedade length
document.write(nome.length)
document.write('<br>')

//propriedade charat
document.write(nome.charAt(12))
document.write('<br>')

//indexof
document.write(nome.indexOf('H'))
document.write('<br>')

//replace
document.write(nome.replace('Henrique', 'lothbruck'))
document.write('<br>')

//substr
document.write(nome.substring(7,15))
document.write('<br>')

//tolowercase
document.write(nome.toLowerCase())
document.write('<br>')

//touppercase
document.write(nome.toUpperCase())
document.write('<br>')

//trim
document.write('-' + nome.trim() + '-')